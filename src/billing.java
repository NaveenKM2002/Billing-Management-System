    private int getLatestBillNumber() throws SQLException {
    int latestBillNumber = 0;
        try (Connection con = ConnectionProvider.getCon();
             Statement st = con.createStatement()) {
            String query = "SELECT MAX(bill_no) AS max_bill_no FROM billing_info";
            ResultSet rs = st.executeQuery(query);

            if (rs.next()) {
                latestBillNumber = rs.getInt("max_bill_no");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            // Handle the exception
            throw e; // You might want to handle or log the exception appropriately
        }
        return latestBillNumber ; // Increment the latest bill number for the new entry
}

