    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                                    
    String phone = jTextField2.getText();
        String paid_amount = jTextField11.getText();
        String return_amount = jTextField12.getText();
        Calendar calendar = Calendar.getInstance();
        Date currentDate = calendar.getTime();
        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement pstBillingInfo = con.prepareStatement("INSERT INTO billing_info (customer_phone, payment_date, paid_amount, return_amount) VALUES (?, ?, ?, ?)");
            pstBillingInfo.setQueryTimeout(30);
            pstBillingInfo.setLong(1, Long.parseLong(phone));
            pstBillingInfo.setTimestamp(2, new Timestamp(currentDate.getTime()));
            pstBillingInfo.setInt(3, Integer.parseInt(paid_amount));
            pstBillingInfo.setInt(4, Integer.parseInt(return_amount));

            pstBillingInfo.executeUpdate();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int rows = model.getRowCount();
            PreparedStatement pstBillingDetails = con.prepareStatement("INSERT INTO billing_details (bill_no, product_id, product_rate, quantity) VALUES (?, ?, ?, ?)");
            int billNo = getLatestBillNumber();
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                int productID = Integer.parseInt(jTable1.getValueAt(i, 0).toString());
                int productRate = Integer.parseInt(jTable1.getValueAt(i, 1).toString());
                int quantity = Integer.parseInt(jTable1.getValueAt(i, 2).toString());

                pstBillingDetails.setInt(1, billNo);
                pstBillingDetails.setInt(2, productID);
                pstBillingDetails.setInt(3, productRate);
                pstBillingDetails.setInt(4, quantity);
                pstBillingDetails.executeUpdate(); // Execute the query to insert into billing_details
            }

            JOptionPane.showMessageDialog(null, "Successfully Updated");

        } catch (SQLException e) {
            e.printStackTrace(); // This will print the exception details
            // Error handling
        }
    }//GEN-LAST:event_jButton2ActionPerformed

